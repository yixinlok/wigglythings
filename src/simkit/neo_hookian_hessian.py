        t2 = np.zeros( (F.shape[0],  9))
        t2[:, [0]] = -la*(F1_2*F3_3-F1_3*F3_2)*(F2_2*F3_3-F2_3*F3_2);
        t2[:, [1]] = mu+la*pow(F1_2*F3_3-F1_3*F3_2,2.0);
