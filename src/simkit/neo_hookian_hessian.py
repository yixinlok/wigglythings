def neo_hookean_filtered_hessian_d2F(F, mu=1, lam=1, vol=1):
    
    dim = F.shape[1]
    mu = np.array(mu).reshape(-1)
    lam = np.array(lam).reshape(-1)
    vol = np.array(vol).reshape(-1)
    
    if dim == 2:
        F_00 = F[:, 0, 0]
        F_01 = F[:, 0, 1]
        F_10 = F[:, 1, 0]
        F_11 = F[:, 1, 1]

        H = np.array(
            [
                [
                    1.0 * F_11**2 * lam,
                    -1.0 * F_10 * F_11 * lam,
                    -1.0 * F_01 * F_11 * lam,
                    1.0 * F_00 * F_11 * lam
                    + 1.0 * lam * (F_00 * F_11 - F_01 * F_10 - 1.0)
                    - mu,
                ],
                [
                    -1.0 * F_10 * F_11 * lam,
                    1.0 * F_10**2 * lam,
                    1.0 * F_01 * F_10 * lam
                    - 1.0 * lam * (F_00 * F_11 - F_01 * F_10 - 1.0)
                    + mu,
                    -1.0 * F_00 * F_10 * lam,
                ],
                [
                    -1.0 * F_01 * F_11 * lam,
                    1.0 * F_01 * F_10 * lam
                    - 1.0 * lam * (F_00 * F_11 - F_01 * F_10 - 1.0)
                    + mu,
                    1.0 * F_01**2 * lam,
                    -1.0 * F_00 * F_01 * lam,
                ],
                [
                    1.0 * F_00 * F_11 * lam
                    + 1.0 * lam * (F_00 * F_11 - F_01 * F_10 - 1.0)
                    - mu,
                    -1.0 * F_00 * F_10 * lam,
                    -1.0 * F_00 * F_01 * lam,
                    1.0 * F_00**2 * lam,
                ],
            ]
        )
    elif dim == 3:
        raise NotImplementedError("3D not implemented")
        
    H = H.transpose(2, 0, 1) * vol.reshape(-1, 1, 1)
    
    return H
    